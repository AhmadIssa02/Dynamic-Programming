package Assignment3;

public class Pointer {//index is the pointer and length is the length of the sequence at a specific point
	int index;
	int length;
	
	public Pointer(int i,int l) {
		index = i;
		length = l;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int i) {
		index = i;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int l) {
		length = l;
	}
	
	
}
